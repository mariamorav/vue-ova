                    <span class="font-bold text-xl">First Conditional</span> <br> <br>
                    <p>
                       El primer condicional es lo más usado de las frases condicionales, y así merece más atención. 
                       Se usa para hablar de cosas que tienen una alta probabilidad de pasar en un futuro. 
                       Cosas posibles y probables.
                        <br><br>
                        La fórmula es: <code> If + presente simple, will + infinitivo.</code>
                        <br><br>
                        Lo que acompaña el if es la condición que se tiene que cumplir para 
                        que suceda ese evento que suponemos.
                        <br><br>
                        <strong>Ejemplos:</strong>

                        <ul>
                            <li>
                                If you don't hurry, you will miss the bus. (Si no te apuras, perderás el bus.)
                            </li>
                            <li>
                                If you start doing exercise you may lose some weight. (Si comienzas a hacer ejercicio puede ser que pierdas peso.)
                            </li> 
                            <li>
                                I you want me to I will go by your house later. (Si tú quires, te visitaré más tarde.)
                            </li>
                            <li>
                                If you don’t like the color I can change it. (si no te gusta el color puedo cambiarlo.)
                            </li>
                        </ul>
                    </p>
