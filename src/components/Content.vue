                    <p>
                        El tercer condicional se utiliza para referirse a una situación en el pasado que no ha ocurrido. En otras palabras, habla de eventos imposibles, porque el pasado no se puede cambiar.
                        <br><br>
                        La fórmula para este condicional es: <code> If + pasado perfecto, would have + participio pasado.</code> 
                        <br><br>
                        <strong>
                            Ejemplos:
                        </strong>
                        <ul>
                            <li>
                                If we had had more time, we would have visited Hawaii. (Si hubiéramos tenido más tiempo, habríamos visitado Hawai.)
                            </li>
                            <li>
                                If I had been able to afford it, I would have bought the Porsche. (Si hubiera podido pagarlo, habría comprado el Porsche.)
                            </li>
                            <li>
                                If they hadn't told me about the exhibition, I never would have known. (Si no me hubieran hablado de la exposición, nunca lo hubiera sabido.)
                            </li>
                        </ul>
                        <br><br>
                        Para resumir, el primer condicional hace referencia a cosas posibles y probables, el segundo condicional refiere a situaciones improbables e hipotéticas y por último el tercer condicional se utiliza para mencionar cosas imposibles. 
