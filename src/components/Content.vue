                <h3 class="text-4xl tracking-tight py-10 md:py-14 font-extrabold text-blue-600 sm:text-5xl">
                    Plus: Condicionales con When, If y As soon as.
                </h3>
                <div class="text-left px-10 md:px-20">
                    <p>
                        Cuando se estudian los condicionales normalmente vemos que las frases comienzan con "if" pero se pueden usar otras expresiones. <br>
                        When, por supuesto, se traduce como “cuándo,” y as soon as se traduce por “tan pronto como.” <br>

                        Así, podemos matizar sobre las cosas futuras. <br><br>

                        <strong>
                            Ejemplos:
                        </strong>

                        <ul>
                            <li>
                                If I see her, I’ll tell her. (Si la veo, se lo diré)
                            </li>
                            <li>
                                When I see her, I’ll tell her. (Cuando la vea, se lo diré)
                            </li>
                            <li>
                                As soon as I see her, I’ll tell her. (Tan pronto como la vea, se lo diré)
                            </li>
                        </ul>

                        <br><br>

                        Mira que When I see her y As soon as I see her utilizan presente, pero hablan del futuro. <br>

                        El futuro se marca con will en la otra parte de la frase.

                        <strong>
                            Más ejemplos:
                        </strong>

                        <ul>
                            <li>
                                If he has enough money, he’ll go to Philippines. (Si él tiene suficiente dinero, se irá a filipinas)
                            </li>
                            <li>
                                When he has enough money, he’ll go to Philippines. (Cuan el tenga suficiente dinero, se irá a filipinas)
                            </li>
                            <li>
                                As soon as he has enough money, he’ll go to Philippines. (Tan pronto como el tenga suficiente dinero, se irá a filipinas)
                            </li>
                        </ul>
