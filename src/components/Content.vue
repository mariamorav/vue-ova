                    <span class="font-bold text-xl">Zero Conditional</span> <br> <br>
                    <p>
                        El condicional cero o Zero Conditional se utiliza para referirse a situaciones reales 
                        y posibles que suceden siempre. Este tipo se utiliza mucho para explicar 
                        aspectos científicos, pero no es tan frecuente para situaciones cotidianas en 
                        la vida de las personas.
                        <br><br>
                        La fórmula que se utiliza es:  <code> If + presente simple, sujeto + presente simple. </code> 
                        <br><br>
                        En esta forma del condicional está permitido sustituir el “if” por “when” 
                        ya que no altera el significado de la oración.  
                        <br><br>
                        <strong> Ejemplos: </strong>
                        <br><br>
                        <ul>
                            <li>
                                If you heat ice, it melts. (Si calientas hielo, se derrite)
                            </li>
                            <li>
                                Plants die if they don’t get enough water. (Las plantas mueren si no obtienen suficiente agua)
                            </li>
                            <li>
                                When you mix red and blue, you get purple. (Cuando mezclas rojo y azul, obtienes violeta)
                            </li>
                        </ul> 
