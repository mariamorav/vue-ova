                    <span class="font-bold text-xl">Second Conditional</span> <br><br>
                    <p>
                        El segundo condicional se utiliza para describir situaciones menos probables. Se puede utilizar de dos maneras distintas. 
                        <br>
                        Por un lado para hablar sobre cosas del futuro que probablemente no ocurran. Por otro lado para hablar sobre cosas del presente que son imposibles. 
                        <br><br>
                        La fórmula es: <code> If + pasado simple, would + infinitivo. </code>
                        <br><br>
                        <strong>
                            Ejemplos:
                        </strong>
                        <ul>
                            <li>
                                If I were taller, she would like me. (Si fuera más alto, le gustaría.)
                            </li>
                            <li>
                                If we lived in France we learned French very fast. (Si viviéramos en Francia aprenderíamos francés muy rápido.)
                            </li>
                            <li>
                                If they were in our place, they would do the same thing. (Si estuvieran en nuestro lugar, harían lo mismo.)
                            </li>
                            <li>
                                If she were your friend, you would tell her the truth. (Si fuera tu amiga, le dirías la verdad.)
                            </li>
                        </ul>
                        <br><br>
                        El segundo condicional se diferencia del primero porque es mucho menos probable que ocurra la situación que se plantea.
                    </p>
                    <p class="py-4 text-gray-700">
                        Para produndizar un poco más en este tipo de condicional te recomendamos ver el siguiente video.
