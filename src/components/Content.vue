                                If he had got up earlier, we wouldn’t be late. (Condicional mixto – Si se hubiera levantado antes, no tendríamos retraso ahora.)
                            </li>
                            <li>
                                If he had got up earlier, we wouldn’t have been late. (Tercer condicional – Llegamos con retraso en el pasado.)
                            </li>
                        </ul>
                        <br>
                        La primera frase es el condicional mixto, que mezcla algo que no se cumplió en el pasado con su hipotético resultado ahora: la primera parte es el tercer condicional y la segunda parte es el segundo condicional.
                        <br><br>
                        En este tipo de oración condicional mixta, el tiempo verbal de la proposición <strong> "if" </strong> es el <strong> "past perfect" </strong> y el verbo de la cláusula principal va en "present conditional". <br>
                        Esta clase de oraciones condicionales mixtas se refieren a una condición irreal situada en el pasado y a su probable resultado en el presente. Expresan una situación que es contraria a 
                        la realidad tanto en el pasado como en el momento actual. En estas oraciones condicionales mixtas, el marco temporal hace referencia al pasado en la proposición "if" y al 
                        presente en la cláusula principal.
